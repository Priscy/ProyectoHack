    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                            
		if (!jTextField1.getText().equals("") && !jTextField2.getText().equals("") && !jTextField3.getText().equals("")){
			if (isNumeric(jTextField1.getText()) && isNumeric(jTextField2.getText()) && isNumeric(jTextField3.getText()) ){
				double a = Double.parseDouble(jTextField1.getText());
				double b = Double.parseDouble(jTextField2.getText());
				double h=  Double.parseDouble(jTextField3.getText());
                                if(a<=0||b<=0||h<=0){
                        jLabel5.setText("los valores deben ser mayores a cero");} 
				
                        else { double resultado = (1.0/3.0)*Math.PI*(h*(Math.pow(a,2)+(a*b)+Math.pow(b,2))); 		
                            
		    	jLabel5.setText("Resultado: " +Double.toString(resultado) +  " ml.");
			}}
                        else{
				jLabel5.setText("Los valores deben ser numÃ©ricos.");
			} 
		}else{
			jLabel5.setText("Debes llenar todos los campos.");
		}
                
    	jLabel5.setVisible(true);
    }                                        
public static boolean isNumeric(String str)
{
  return str.matches("-?\\d+(\\.\\d+)?");  //match a number with optional '-' and decimal.
}
